package java1;

import static java.lang.Math.PI;
public class Task14 {

	public static void main(String[] args) {
		
		double r = 5;
		double s; //Square
		double a; //Length
		
		s = (PI * (r * r));
		a = (2 * PI * r);
		
		System.out.println(r * r * PI + "=" + s);
		System.out.println(2 * PI * r + "=" + a);
				
	}

}
